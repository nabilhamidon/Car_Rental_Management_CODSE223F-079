    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        
     if("".equals(txt_days.getText()))
       {
            JOptionPane.showMessageDialog(null,"select number of days to generate full price", "VehicleManagement", JOptionPane.INFORMATION_MESSAGE);
       
       }
     else if("".equals(txt_price.getText()))
     {
          JOptionPane.showMessageDialog(null,"select a vehicle", "VehicleManagement", JOptionPane.INFORMATION_MESSAGE);
       
     }
     else if(isFloat(txt_days.getText()))
     {
         float fine = Float.valueOf(txt_days.getText()) * Float.valueOf(txt_price.getText());
        txt_bill.setText(Float.toString(fine));
     }
     else{
        JOptionPane.showMessageDialog(null,"Please enter only numbers for rental days", "VehicleManagement", JOptionPane.INFORMATION_MESSAGE);
       
        
       }   // TODO add your handling code here:
    }//GEN-LAST:event_jLabel14MouseClicked

