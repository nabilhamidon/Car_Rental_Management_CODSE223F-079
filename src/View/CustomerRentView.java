    public void displayVehicles() 
    {  ResultSet rst;
       String vehiclestatus= "Available";
        try{
        
        Statement st=MDBConnection.createDBConnection().createStatement();
        rst= st.executeQuery("select RegNo, Brand,Model, Type, Price from vehicletbl where Availability='"+vehiclestatus+"'");
       tbl_vehicles.setModel(DbUtils.resultSetToTableModel(rst));
        }
        catch(SQLException e)
        {
            e.printStackTrace();
        }
    }
    
